#include <libft.h>

void	*ft_memset(void *s, int c, size_t n)
{
	char	*aux;
	size_t	i;

	i = 0;
	aux = (char *)s;
	while (n > 0)
	{
		aux[n - 1] = c;
		n--;
	}
	return (s);
}
